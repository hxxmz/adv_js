function populateTable() {
    const tbody = document.querySelector('.body');
    extensions.forEach((extension, index) => {
        const tr = document.createElement('tr');
        tr.innerHTML = `
            <th scope="row">${index + 1}</th>
            <td>${extension.name}</td>
            <td>${extension.department}</td>
            <td>${extension.extension}</td>
        `;
        tbody.appendChild(tr);
    });
}

function findExtension() {
    const inputVal = document.getElementById('extNum').value;
    const tbody = document.querySelector('.body');
    tbody.innerHTML = '';

    //const filteredExtensions = extensions.filter(extension => extension.extension === inputVal);
    var pattern = new RegExp("\\b"+inputVal+"[0-9]*","gm"); 
    const filteredExtensions = extensions.filter(ext => pattern.test(ext["extension"]));

    filteredExtensions.sort( (a, b) => {
        return a.extension - b.extension;
    })

    filteredExtensions.forEach((extension, index) => {
        const tr = document.createElement('tr');
        tr.innerHTML = `
            <th scope="row">${index + 1}</th>
            <td>${extension.name}</td>
            <td>${extension.department}</td>
            <td>${extension.extension}</td>
        `;
        tbody.appendChild(tr);
    });
    setInterval(() => {
        if(inputVal === '') {
            populateTable();
        }
    }, 2000);
}

